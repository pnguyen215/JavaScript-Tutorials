// case 1: using for loop 
console.log("For loop")
for (let i = 0; i < 10; i++) {
    console.log(i)
}

// case 2: using while loop
console.log("While loop")
let i = 0
while (i < 10) {
    console.log(i)
    i++
}

// case 3: using do while loop
console.log("Do while loop")
let j = 0
do {
    console.log(j)
    j++
} while (j < 10);