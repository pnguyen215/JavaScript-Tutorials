/**
 * The JavaScript math floor() method decreases the given number up to the closest integer value and returns it.
 * If the given number is already an integer, the floor() method returns it directly.
 * It returns largest integer value, lower than or equal to the given number.
 */
console.log(Math.floor(-3.4)) // -4
console.log(Math.floor(16.3)) // 16
console.log(Math.floor(15.7)) // 15
console.log(Math.floor(-0.2)) // -1