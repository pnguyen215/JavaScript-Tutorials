/**
 * The JavaScript math round() method rounds up the given number up to the closest integer value and returns it.
 * If the given number is already an integer, the round() method returns it directly.
 */

// to increases the given number up to the closest integer value
console.log(Math.round(2.4)) // 2
console.log(Math.round(0.2)) // 0
console.log(Math.round(0.5)) // 1   
console.log(Math.round(-4.5)) // -4
console.log(Math.round(-0.6)) // -1