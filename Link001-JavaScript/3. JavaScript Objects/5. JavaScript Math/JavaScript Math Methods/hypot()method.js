/**
 * The JavaScript math hypot() method returns the square root of sum of the squares of given numbers.
 * It returns NaN, if any argument cannot be converted to a number.
 * It returns square root of sum of the squares of given numbers
 */

console.log(Math.hypot(3, 4)) // sqrt(3*3+4*4) = 5
console.log(Math.hypot(6, 9))