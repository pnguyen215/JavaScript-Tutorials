/**
 * The JavaScript math ceil() method increases the given number up to the closest integer value and returns it
 * If the given number is already an integer, the ceil() method returns it directly.
 */

console.log(Math.ceil(6.5)) // 7
console.log(Math.ceil(0.3)) // 1
console.log(Math.ceil(-0.2)) // -0
console.log(Math.ceil(-5.3)) // -5